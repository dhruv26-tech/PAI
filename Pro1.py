# Understanding variables, input (), print (), type() functions...


name = input ("Enter Name: ")
print (name)
print (type (name))

i = int (input ("Enter any integer: "))
print (i)

print (type (i))

f = float (input ("Enter any floating number: "))
print (f)
print (type (f))

b = bool (input ("Is 2 + 2 = 4 ? State True or False: "))
print (b)
print (type (b))